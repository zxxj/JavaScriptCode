// +
var result = 3 + 3;
console.log(result); // 6

// -
var result2 = 3 - 1;
console.log(result2); // 2

// *
var result3 = 3 * 3;
console.log(result3); // 9

// /
var result4 = 4 / 2;
console.log(result4); // 2

// %
var result5 = 10 % 5;
console.log(result5); // 0

// **: 幂
var result6 = 2 ** 3;
console.log(result6); // 8, 相当于2的3次方, 用Math.pow同样能实现
console.log(Math.pow(2, 3)); // 8
